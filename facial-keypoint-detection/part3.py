# Rescale the prediction points to original image
def rescale_predictions(preds, bbox):
  left, top, bound_w, bound_h = np.array(bbox).astype(int)
  for pred in preds:
    pred[0] = pred[0] * bound_w + left
    pred[1] = pred[1] * bound_h + top
  return preds

